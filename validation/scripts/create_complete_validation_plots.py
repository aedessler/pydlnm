    summary_df = pd.read_csv('/Users/adessler/Desktop/DLNM/complete_rr_curves_summary.csv')
    
    # Create figure with subplots
    fig, ((ax1, ax2), (ax3, ax4)) = plt.subplots(2, 2, figsize=(15, 10))
